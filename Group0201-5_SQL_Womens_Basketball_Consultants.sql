CREATE TABLE Game (
	gameNo SMALLINT NOT NULL,
	dayNo SMALLINT NOT NULL,
	month CHAR(3) NOT NULL,
	year SMALLINT NOT NULL,
	weekDay CHAR(3) NOT NULL,
	CONSTRAINT pk_Game_gameNo PRIMARY KEY (gameNo)
);

CREATE Table Location (
	locName VARCHAR(30) NOT NULL,
	gameNo SMALLINT NOT NULL,
	PRIMARY KEY (locName, gameNo)
);

CREATE Table Score (
	scoreNo INTEGER NOT NULL,
	pointsFor INTEGER NOT NULL,
	pointsAgainst INTEGER NOT NULL,
	pointDifferential INTEGER NOT NULL,
	overtimeBin INTEGER,
	winBin CHAR(1),
	gameNo SMALLINT NOT NULL,
	PRIMARY KEY (scoreNo),
	CONSTRAINT fk_Score_gameNo FOREIGN KEY (gameNo) 
  REFERENCES Game (gameNo)
);

CREATE Table Opponent (
	opName VARCHAR(30) NOT NULL,
	gameNo SMALLINT NOT NULL,
	PRIMARY KEY (opName, gameNo)
	);

CREATE TABLE Ranking (
	rankingNo INTEGER NOT NULL,
	opRanking INTEGER,
	marylandRanking INTEGER,
	gameNo SMALLINT NOT NULL,
	PRIMARY KEY (rankingNo),
	CONSTRAINT fk_Ranking_gameNo FOREIGN KEY (gameNo)
  REFERENCES Game (gameNo)
);

