--Mission Objectives:
--Basic objectives:
--For a selected opponent school, how many times have we won from 1998-2015?
SELECT COUNT(*)
FROM Score s, Opponent o
WHERE s.gameNo = o.gameNo AND
			o.opName = 'Duke' AND
			s.WinBin = 'W';
--Answer (Duke): 10

--What were the number of games in which we were ranked higher than our opponents from 1998-2015?
SELECT COUNT(*)
FROM Game g, Ranking r
WHERE g.gameNo = r.gameNo AND
			r.marylandRanking IS NOT NULL AND
      (r.marylandRanking < r.opRanking OR r.opRanking IS NULL) AND
      g.year BETWEEN 1998 AND 2015;
--Answer: 75

--Reporting objectives:
--For a certain year, how many opponents did we play?
SELECT COUNT(DISTINCT o.opName)
FROM Opponent o, Game g
WHERE o.gameNo = g.gameNo AND
			g.year = 2015;
--Answer (2015): 29

--For a certain year, how many games did we play on Sunday?
SELECT COUNT(*)
FROM Game g
WHERE g.weekDay = 'Sun' AND
			g.year = 2014;
--Answer (2014): 12

